                        El Dispositivo Portátil de 
                        Portales de Aperture Science,es la herramienta 
                        experimental diseñada para poner dos portales por los 
                        que pueden pasar objetos.
                        El Dispositivo Portátil de Portales está diseñado para 
                        crear portales en cualquier superficie blanca que sea 
                        inmóvil, plana y suficientemente grande. Cuando es 
                        disparado, un proyectil colorado será expulsado por un
                        barril. Si el proyectil da a una superficie válida, un 
                        portal del color correspondiente será abierto. Si es 
                        abierto otro portal de distinto color, ambos serán 
                        conectados. Si un portal del mismo color es colocado, 
                        se cerrará y se abrirá el nuevo.
